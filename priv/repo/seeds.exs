# Script for populating the database. You can run it as:
#
#     mix run priv/repo/seeds.exs
#
# Inside the script, you can read and write to any of your
# repositories directly:
#
#     Krite.Repo.insert!(%Krite.SomeSchema{})
#
# We recommend using the bang functions (`insert!`, `update!`
# and so on) as they will fail if something goes wrong.

defmodule Krite.RepoSeedUtil do
  alias Krite.Accounts.Kveg
  alias Krite.Products.Item

  def make_kveg({firstname, lastname, email, balance}) do
    Kveg.changeset(
      %Kveg{},
      %{
        firstname: firstname,
        lastname: lastname,
        email: email,
        balance: balance
      }
    )
  end
    
  def make_item({barcode, name, price}) do
    ean_upc_barcode = if valid_ean_upc_barcode?(barcode), do: barcode
      
    Item.changeset(
      %Item{},
      %{
        name: name,
        barcode: ean_upc_barcode,
        price: price,
      }
    )
  end

  defp valid_ean_upc_barcode?(barcode) do
    with {_number, ""} <- Integer.parse(barcode) do
      String.length(barcode) in [8, 13]
    else
      _ -> false
    end
  end
end

